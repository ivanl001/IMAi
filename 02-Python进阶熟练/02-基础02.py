
##### 数序函数

''''''
import math
print(abs(-10))#绝对值

print(max(1,2,4,1.2, 4,2))#返回给定的最大参数
print(min(0.4, 2,3,4))#返回最小的给定参数

print(round(3.22))#四舍五入

print(math.fabs(-10))#也是绝对值
print(math.ceil(4.1))#上入整数
print(math.floor(9.3))#下舍整数
print(math.exp(1))#e的1次幂
print(math.log(100, 10))#求log，也就是log10 100，结果为2
print(math.log10(100))#求log10，结果为2
print(math.pow(2,3))#2的3次方
print(math.modf(3.42))#返回整数部分和小数部分
print(math.sqrt(3))#返回平方根
